def main() -> None:
    missing: list[str] = []
    for relative_path, fragments in REQUIRED.items():
        path = ROOT / relative_path
        if not path.exists():
            missing.append(f"{relative_path}: file missing")
            continue
        content = path.read_text(encoding="utf-8")
        for fragment in fragments:
            if fragment not in content:
                missing.append(f"{relative_path}: missing {fragment!r}")
    if missing:
        raise SystemExit("FAIL stage2 customer service evaluation:\n" + "\n".join(missing))
    print("PASS stage2 customer service evaluation")


if __name__ == "__main__":
    main()
